package smartcupon.modelo.pojo;

/**
 *
 * @author Dell
 */
public class Direccion {
    private Integer idDireccion;
    private String calle;
    private Integer numero;
    private String colonia;
    private String codigoPostal;
    private Integer ciudad;
    private Integer tipoDireccion;
    private Integer estado;
    
    public Direccion() {
    }

    public Direccion(Integer idDireccion, String calle, Integer numero, String colonia, String codigoPostal, Integer ciudad, Integer tipoDireccion, Integer estado) {
        this.idDireccion = idDireccion;
        this.calle = calle;
        this.numero = numero;
        this.colonia = colonia;
        this.codigoPostal = codigoPostal;
        this.ciudad = ciudad;
        this.tipoDireccion = tipoDireccion;
        this.estado = estado;
    }

    public Integer getIdDireccion() {
        return idDireccion;
    }

    public void setIdDireccion(Integer idDireccion) {
        this.idDireccion = idDireccion;
    }

    public String getCalle() {
        return calle;
    }

    public void setCalle(String calle) {
        this.calle = calle;
    }

    public Integer getNumero() {
        return numero;
    }

    public void setNumero(Integer numero) {
        this.numero = numero;
    }

    public String getColonia() {
        return colonia;
    }

    public void setColonia(String colonia) {
        this.colonia = colonia;
    }

    public String getCodigoPostal() {
        return codigoPostal;
    }

    public void setCodigoPostal(String codigoPostal) {
        this.codigoPostal = codigoPostal;
    }

    public Integer getCiudad() {
        return ciudad;
    }

    public void setCiudad(Integer ciudad) {
        this.ciudad = ciudad;
    }

    public Integer getTipoDireccion() {
        return tipoDireccion;
    }

    public void setTipoDireccion(Integer tipoDireccion) {
        this.tipoDireccion = tipoDireccion;
    }

    public Integer getEstado() {
        return estado;
    }

    public void setEstado(Integer estado) {
        this.estado = estado;
    }
    
    
}
